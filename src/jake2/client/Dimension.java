package jake2.client;

public class Dimension {

  public int width, height;

  public Dimension() {
  }

  public Dimension(int width, int height) {
    this.width = width;
    this.height = height;
  }
  
  public int getHeight() {
    return height;
  }

  public int getWidth() {
		return width;
  }
}
